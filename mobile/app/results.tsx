import React, { useState, useMemo } from 'react';
import {
  View,
  Text,
  StyleSheet,
  ScrollView,
  TouchableOpacity,
  Share,
} from 'react-native';
import { useLocalSearchParams, useRouter } from 'expo-router';
import { Ionicons } from '@expo/vector-icons';
import { SafeAreaView } from 'react-native-safe-area-context';
import { Colors } from '@/constants/Colors';
import RiskBadge from '@/components/RiskBadge';
import ConditionBar from '@/components/ConditionBar';
import DiagnosisCard from '@/components/DiagnosisCard';
import ECGWaveform from '@/components/ECGWaveform';
import FeedbackModal from '@/components/FeedbackModal';
import type { AnalysisResult } from '@/types';

// ── helpers ──────────────────────────────────────────────────────────────────

function fmt(v: number | undefined | null, decimals = 0, unit = '') {
  if (v === undefined || v === null || isNaN(v)) return '—';
  return `${v.toFixed(decimals)}${unit}`;
}

// ── component ─────────────────────────────────────────────────────────────────

export default function ResultsScreen() {
  const params = useLocalSearchParams<{ data: string }>();
  const router = useRouter();
  const [feedbackVisible, setFeedbackVisible] = useState(false);
  const [showAllConditions, setShowAllConditions] = useState(false);

  const result = useMemo<AnalysisResult | null>(() => {
    if (!params.data) return null;
    try {
      return JSON.parse(params.data);
    } catch {
      return null;
    }
  }, [params.data]);

  if (!result) {
    return (
      <View style={styles.errorContainer}>
        <Ionicons name="alert-circle" size={48} color={Colors.danger} />
        <Text style={styles.errorText}>Could not load results.</Text>
        <TouchableOpacity onPress={() => router.back()}>
          <Text style={styles.backLink}>Go back</Text>
        </TouchableOpacity>
      </View>
    );
  }

  const { risk_level, shd_probability, ecg_features, probabilities, labels,
          diagnoses, ptbxl_result, waveform, lead_names, waveform_meta } = result;

  const topDiagnosis = diagnoses?.[0]?.name;

  // Composite SHD (index 11) + first 11 conditions
  const compositeProb = probabilities[11] ?? 0;
  const conditions = labels
    .slice(0, 11)
    .map((label, i) => ({ label, probability: probabilities[i] ?? 0 }))
    .sort((a, b) => b.probability - a.probability);

  const visibleConditions = showAllConditions ? conditions : conditions.slice(0, 5);

  async function handleShare() {
    const lines = [
      `ECG AI Interpretation`,
      `Risk: ${risk_level} (${Math.round(shd_probability * 100)}% SHD probability)`,
      topDiagnosis ? `Top finding: ${topDiagnosis}` : '',
      `Heart rate: ${fmt(ecg_features?.heart_rate, 0, ' bpm')}`,
      `QRS: ${fmt(ecg_features?.qrs_duration, 0, ' ms')}  QTc: ${fmt(ecg_features?.qtc_interval, 0, ' ms')}`,
      '',
      'Generated by ECG Interpreter (research use only)',
    ].filter(Boolean).join('\n');

    await Share.share({ message: lines });
  }

  return (
    <SafeAreaView style={styles.root} edges={['bottom']}>
      <ScrollView contentContainerStyle={styles.content} showsVerticalScrollIndicator={false}>

        {/* ── Risk badge ── */}
        <RiskBadge level={risk_level} probability={shd_probability} />

        {/* ── ECG features grid ── */}
        <View style={styles.card}>
          <Text style={styles.cardTitle}>ECG Features</Text>
          <View style={styles.featuresGrid}>
            {[
              { label: 'Heart Rate', value: fmt(ecg_features?.heart_rate, 0, ' bpm') },
              { label: 'QRS Duration', value: fmt(ecg_features?.qrs_duration, 0, ' ms') },
              { label: 'QTc Interval', value: fmt(ecg_features?.qtc_interval, 0, ' ms') },
              { label: 'PR Interval', value: fmt(ecg_features?.pr_interval, 0, ' ms') },
              { label: 'Age', value: ecg_features?.age ? `${Math.round(ecg_features.age)} yr` : '—' },
              { label: 'Sex', value: ecg_features?.sex ?? '—' },
            ].map(f => (
              <View key={f.label} style={styles.featureCell}>
                <Text style={styles.featureValue}>{f.value}</Text>
                <Text style={styles.featureLabel}>{f.label}</Text>
              </View>
            ))}
          </View>
        </View>

        {/* ── 12-lead waveform ── */}
        {waveform && waveform.length > 0 && (
          <ECGWaveform waveform={waveform} leadNames={lead_names ?? []} />
        )}

        {/* ── Diagnoses ── */}
        {diagnoses && diagnoses.length > 0 && (
          <View style={styles.card}>
            <Text style={styles.cardTitle}>Clinical Interpretation</Text>
            {diagnoses.map((d, i) => (
              <DiagnosisCard key={i} diagnosis={d} />
            ))}
          </View>
        )}

        {/* ── Condition probabilities ── */}
        <View style={styles.card}>
          <Text style={styles.cardTitle}>Condition Probabilities</Text>
          <ConditionBar label="Any Structural Heart Disease" probability={compositeProb} isComposite />
          <View style={styles.separator} />
          {visibleConditions.map(c => (
            <ConditionBar key={c.label} label={c.label} probability={c.probability} />
          ))}
          {conditions.length > 5 && (
            <TouchableOpacity
              style={styles.showMoreBtn}
              onPress={() => setShowAllConditions(v => !v)}
            >
              <Text style={styles.showMoreText}>
                {showAllConditions ? 'Show less' : `Show all ${conditions.length} conditions`}
              </Text>
              <Ionicons
                name={showAllConditions ? 'chevron-up' : 'chevron-down'}
                size={14}
                color={Colors.primary}
              />
            </TouchableOpacity>
          )}
        </View>

        {/* ── PTB-XL ECG patterns ── */}
        {ptbxl_result && ptbxl_result.probabilities.length > 0 && (
          <View style={styles.card}>
            <Text style={styles.cardTitle}>ECG Pattern Analysis (PTB-XL)</Text>
            {ptbxl_result.labels.map((label, i) => (
              <ConditionBar key={label} label={label} probability={ptbxl_result.probabilities[i] ?? 0} />
            ))}
          </View>
        )}

        {/* ── Waveform meta ── */}
        <View style={styles.metaRow}>
          <Ionicons name="information-circle-outline" size={14} color={Colors.textDisabled} />
          <Text style={styles.metaText}>
            {waveform_meta?.leads_detected ?? 12} leads detected · Source: {waveform_meta?.source ?? 'uploaded image'}
          </Text>
        </View>

        {/* ── Actions ── */}
        <View style={styles.actions}>
          <TouchableOpacity style={styles.feedbackBtn} onPress={() => setFeedbackVisible(true)}>
            <Ionicons name="star-outline" size={18} color={Colors.primary} />
            <Text style={styles.feedbackBtnText}>Rate this interpretation</Text>
          </TouchableOpacity>

          <TouchableOpacity style={styles.shareBtn} onPress={handleShare}>
            <Ionicons name="share-outline" size={18} color={Colors.white} />
            <Text style={styles.shareBtnText}>Share Summary</Text>
          </TouchableOpacity>
        </View>

        <TouchableOpacity style={styles.newBtn} onPress={() => router.replace('/')}>
          <Ionicons name="add-circle-outline" size={18} color={Colors.primary} />
          <Text style={styles.newBtnText}>Analyse Another ECG</Text>
        </TouchableOpacity>

        {/* Disclaimer */}
        <Text style={styles.disclaimer}>
          For research and educational use only. Not a certified medical device.
          Confirm all findings with a qualified healthcare professional.
        </Text>
      </ScrollView>

      <FeedbackModal
        visible={feedbackVisible}
        onClose={() => setFeedbackVisible(false)}
        riskLevel={risk_level}
        topDiagnosis={topDiagnosis}
      />
    </SafeAreaView>
  );
}

